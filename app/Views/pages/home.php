    <p class="mt-3 text-center">
        Pharmacie Humanitaire Internationale est une association française de solidarité internationale, à but non lucratif, fondée en 1985 sous le nom de Pharmaciens Sans Frontières. Régie par la loi 1901, elle rassemble aujourd’hui 35 associations locales réparties dans 47 départements, avec des actions menées dans 40 pays, principalement en Afrique et en Europe. 
        Portée par des pharmaciens bénévoles convaincus que l’accès aux soins et aux médicaments est un droit fondamental, P.H.I. agit sur deux fronts. En France, elle est reconnue seule responsable de l’approvisionnement en médicaments neufs auprès des centres de soins caritatifs. À l’étranger, elle facilite l’accès aux soins pour les populations les plus fragiles, en privilégiant les circuits d'approvisionnement locaux pour préserver l’économie des pays concernés. 
        Au-delà de l’aide au développement sanitaire, P.H.I. intervient lors de catastrophes naturelles ou de conflits - comme en Ukraine ou à Mayotte - et peut financer, dans des conditions strictes de partenariat, la construction de structures de santé à l’étranger, comme la maternité de Mboyo au Sénégal.
